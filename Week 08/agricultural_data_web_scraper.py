def analyze_extracted_data(scraper):
    """Analyze the extracted data for insights"""
    if not scraper.extracted_data:
        print("No data to analyze")
        return

    print("\n🔍 DATA ANALYSIS SUMMARY:")

    total_tables = sum(len(item['structured'].get('tables', [])) for item in scraper.extracted_data)
    total_lists = sum(len(item['structured'].get('lists', [])) for item in scraper.extracted_data)
    relevant_items = sum(1 for item in scraper.extracted_data if item['structured'].get('is_relevant', False))

    print(f"Total tables extracted: {total_tables}")
    print(f"Total lists extracted: {total_lists}")
    print(f"Relevant content found in: {relevant_items}/{len(scraper.extracted_data)} URLs")

    # Show URLs with most content
    url_content_scores = []
    for item in scraper.extracted_data:
        score = len(item['structured'].get('tables', [])) * 3 + len(item['structured'].get('lists', []))
        url_content_scores.append((item['url'], score))

    url_content_scores.sort(key=lambda x: x[1], reverse=True)

    print(f"\n🏆 Top 5 URLs by content richness:")
    for url, score in url_content_scores[:5]:
        print(f"Score {score}: {url}")

analyze_extracted_data(scraper)

